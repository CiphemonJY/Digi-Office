#!/usr/bin/env python3
"""
Generate PNG sprite sheets from the built-in pixel art data.

Usage:
    cd digi_office/static/sprites
    pip install Pillow
    python generate_sheets.py

Output:  ciphemon.png  hermes.png  worker.png
Format:  rows = animation states (idle / work / sleep / tool)
         cols = frames in that state (up to MAX_FRAMES)
         each cell = FRAME_PX × FRAME_PX pixels

After running, replace any PNG with your own art and the dashboard
will load it automatically. See docs/SPRITES.md for the full guide.
"""

import os
from PIL import Image  # pip install Pillow

SCALE      = 4          # pixels per sprite-pixel (12×12 → 48×48)
FRAME_PX   = 12 * SCALE # 48 px per frame
MAX_FRAMES = 2          # columns in every row
STATES     = ["idle", "work", "sleep", "tool"]

OUT_DIR = os.path.dirname(os.path.abspath(__file__))


# ── Helpers ────────────────────────────────────────────────────────────

def hex_to_rgba(h: str) -> tuple:
    h = h.lstrip("#")
    return tuple(int(h[i:i+2], 16) for i in (0, 2, 4)) + (255,)


def render_frame(grid: list, palette: list) -> Image.Image:
    """Render a 12×12 integer grid to a FRAME_PX×FRAME_PX RGBA image."""
    img = Image.new("RGBA", (FRAME_PX, FRAME_PX), (0, 0, 0, 0))
    pixels = img.load()
    for ry, row in enumerate(grid):
        for rx, c in enumerate(row):
            if c == 0 or palette[c] is None:
                continue
            color = hex_to_rgba(palette[c])
            for dy in range(SCALE):
                for dx in range(SCALE):
                    pixels[rx * SCALE + dx, ry * SCALE + dy] = color
    return img


def make_sheet(name: str, sprite_data: dict, palette: list) -> str:
    """Assemble all states into a single sprite sheet PNG."""
    rows = len(STATES)
    cols = MAX_FRAMES
    sheet = Image.new("RGBA", (FRAME_PX * cols, FRAME_PX * rows), (0, 0, 0, 0))

    for row_idx, state in enumerate(STATES):
        frames = sprite_data.get(state, sprite_data.get("idle", []))
        for col_idx, grid in enumerate(frames[:MAX_FRAMES]):
            frame = render_frame(grid, palette)
            sheet.paste(frame, (col_idx * FRAME_PX, row_idx * FRAME_PX))

    path = os.path.join(OUT_DIR, f"{name}.png")
    sheet.save(path, "PNG")
    print(f"  OK  {path}  ({sheet.width}x{sheet.height})")
    return path


